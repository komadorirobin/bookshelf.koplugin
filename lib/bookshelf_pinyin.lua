-- tools/gen_pinyin_table.py from Unihan_Readings.txt):
--   * Source: Unicode 17.0.0 Unihan kMandarin (one preferred reading per
--     character; polyphones get that single reading, which is fine for
--     sorting). Tones stripped; u-umlaut folds to plain u, so lu(:)
--     homographs tie and fall to the codepoint tie-break.
--   * Coverage: the CJK Unified Ideographs block U+4E00..U+9FFF (20992
--     codepoints, 20924 with a reading). Extension blocks are rare in
--     names and would triple the table; uncovered chars pass through
--     unchanged (codepoint order).
--   * SYL is the sorted array of the 411 distinct toneless syllables.
--   * DATA holds one base64-alphabet character PAIR per codepoint:
--     index = hi*64 + lo into SYL, 0 = no reading. Lookups do byte math
--     straight into the packed string -- no 21k-entry Lua table is ever
--     built, so the memory cost is just the ~41KB string itself.

