--
-- Without this, spaceless CJK strings sort by raw UTF-8 bytes, i.e.
-- Unicode codepoint order -- which is approximately radical/stroke order
-- (a legitimate dictionary collation, and the default), but looks random
-- to a reader expecting pinyin order, and interleaves nowhere near Latin
-- authors. With the toggle on, M.key() rewrites each Han character to its
-- toneless Mandarin syllable so 刘慈欣 keys as "liu ci xin " and files
