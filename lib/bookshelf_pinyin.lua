-- Reverse map: base64-alphabet byte -> 0..63.
local ALPHA = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/"
local B64 = {}
for i = 1, 64 do B64[ALPHA:byte(i)] = i - 1 end

local URO_LO, URO_HI = 0x4E00, 0x9FFF

-- Toneless Mandarin syllable for a URO codepoint, or nil when Unihan has
-- no kMandarin reading (or the codepoint is outside the table).
local function sylOf(cp)
    if cp < URO_LO or cp > URO_HI then return nil end
    local off = (cp - URO_LO) * 2
    local idx = B64[DATA:byte(off + 1)] * 64 + B64[DATA:byte(off + 2)]
    if idx == 0 then return nil end
    return SYL[idx]
end
M.sylOf = sylOf  -- exposed for tests

-- Cheap pre-filter: can M.key() possibly change this string? Lead bytes
-- 0xE4..0xE9 cover 3-byte UTF-8 sequences U+4000..U+9FFF, a superset of
-- the URO -- false positives just mean key() runs and returns s unchanged.
function M.hasHan(s)
    return s:find("[\228-\233]", 1) ~= nil
end

-- key(s): the pinyin sort key for `s`. Every Han character with a known
-- reading becomes its toneless syllable plus a space (space sorts below
-- all letters, so multi-character names compare syllable-by-syllable:
-- "liu ci " < "liu xin "). All other characters -- ASCII, accented Latin,
-- kana, hangul, unmapped Han -- pass through untouched. When at least one
-- character converted, the ORIGINAL string is appended after a \1
-- separator as a tie-break, so same-syllable characters (刘/留, both
-- "liu") still order deterministically by codepoint. Strings with no Han
-- content come back unchanged (===), so non-CJK libraries pay only the
-- hasHan() scan.
function M.key(s)
    if not s or s == "" then return s end
    local out, n   = {}, 0
    local i, len   = 1, #s
    local changed  = false
    while i <= len do
        local c = s:byte(i)
        if c < 0x80 then
            n = n + 1; out[n] = s:sub(i, i)
            i = i + 1
        elseif c >= 0xE4 and c <= 0xE9 and i + 2 <= len then
            local c2, c3 = s:byte(i + 1), s:byte(i + 2)
            local cp = (c - 0xE0) * 0x1000 + (c2 - 0x80) * 0x40 + (c3 - 0x80)
            local syl = sylOf(cp)
            if syl then
                changed = true
                n = n + 1; out[n] = syl
                n = n + 1; out[n] = " "
            else
                n = n + 1; out[n] = s:sub(i, i + 2)
            end
            i = i + 3
        else
            -- Copy any other UTF-8 sequence whole.
            local step = (c < 0xC0 and 1) or (c < 0xE0 and 2)
                      or (c < 0xF0 and 3) or 4
            n = n + 1; out[n] = s:sub(i, i + step - 1)
            i = i + step
        end
    end
    if not changed then return s end
    return table.concat(out) .. "\1" .. s
end

return M
